#include "philo.h"

int only_nb_atoi(char *s)
{
	long nbs;

	nbs = 0;
	while(*s)
	{
		if (*s > '0' || '9' < *s)
			return (-1);
		nbs = nbs * 10 + (*s - '0');
		s++;
	}
	return(nbs);
}